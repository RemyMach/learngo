package main

import (
	"fmt"
	"strings"
)

// ---------------------------------------------------------
// EXERCISE: Case Insensitive Search
//
//  Allow for case-insensitive searching
//
// EXAMPLE
//  Let's say that the user runs the program like this:
//    go run main.go LAZY
//
//  Or like this: go run main.go lAzY
//  Or like this: go run main.go lazy
//
//  For all cases above, the program should find
//  the "lazy" keyword.
// ---------------------------------------------------------

const matchString = "lazy"

func main() {

	const word = "laZy"

	if strings.ToLower(word) == matchString {
		fmt.Printf("Congratulations you find the word")
		return
	}

	fmt.Printf("fail you dont find the word")
}
